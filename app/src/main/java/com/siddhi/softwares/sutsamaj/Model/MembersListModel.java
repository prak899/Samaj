package com.siddhi.softwares.sutsamaj.Model;

public class MembersListModel {
    String name, relation, gender;
    int age, cast_certificate, education, employment;



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRelation() {
        return relation;
    }

    public void setRelation(String relation) {
        this.relation = relation;
    }


    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getCast_certificate() {
        return cast_certificate;
    }

    public void setCast_certificate(int cast_certificate) {
        this.cast_certificate = cast_certificate;
    }

    public int getEducation() {
        return education;
    }

    public void setEducation(int education) {
        this.education = education;
    }

    public int getEmployment() {
        return employment;
    }

    public void setEmployment(int employment) {
        this.employment = employment;
    }
}
