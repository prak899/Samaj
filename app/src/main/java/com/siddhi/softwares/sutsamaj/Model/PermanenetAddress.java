package com.siddhi.softwares.sutsamaj.Model;

public class PermanenetAddress {
    String p_address;
    int p_state, p_district, p_tehsil, p_village, p_pin;

    public String getP_address() {
        return p_address;
    }

    public void setP_address(String p_address) {
        this.p_address = p_address;
    }

    public int getP_state() {
        return p_state;
    }

    public void setP_state(int p_state) {
        this.p_state = p_state;
    }

    public int getP_district() {
        return p_district;
    }

    public void setP_district(int p_district) {
        this.p_district = p_district;
    }

    public int getP_tehsil() {
        return p_tehsil;
    }

    public void setP_tehsil(int p_tehsil) {
        this.p_tehsil = p_tehsil;
    }

    public int getP_village() {
        return p_village;
    }

    public void setP_village(int p_village) {
        this.p_village = p_village;
    }

    public int getP_pin() {
        return p_pin;
    }

    public void setP_pin(int p_pin) {
        this.p_pin = p_pin;
    }
}
